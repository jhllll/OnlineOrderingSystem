package com.onlineordering.po;

public class Customer {
	private int cusId;
	private String cusName;
	private String cusPwd;
	private String cusAdr;
	private String cusPnum;
	public int getCusId() {
		return cusId;
	}
	public void setCusId(int cusId) {
		this.cusId = cusId;
	}
	public String getCusName() {
		return cusName;
	}
	public void setCusName(String cusName) {
		this.cusName = cusName;
	}
	public String getCusPwd() {
		return cusPwd;
	}
	public void setCusPwd(String cusPwd) {
		this.cusPwd = cusPwd;
	}
	public String getCusAdr() {
		return cusAdr;
	}
	public void setCusAdr(String cusAdr) {
		this.cusAdr = cusAdr;
	}
	public String getCusPnum() {
		return cusPnum;
	}
	public void setCusPnum(String cusPnum) {
		this.cusPnum = cusPnum;
	}
	
}
